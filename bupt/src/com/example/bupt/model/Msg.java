package com.example.bupt.model;

import java.util.Date;


public class Msg extends Entity{
	
	public static final String MSG_ID = "msg_id";
	public static final String MSG_FROM = "from_id";
	public static final String MSG_NAME = "name";
	public static final String MSG_FACE = "face";
	public static final String MSG_CONTENT = "content";
	public static final String MSG_TIME = "ctime";
	public static final String MSG_TYPE = "type";

	private int msgId;
	private int fromId;
	private String fromName;
	private String fromFace;
	private String content;
	private Date ctime;
	private int msgType;
	private int msgCount;
	
	public int getMsgId() {
		return msgId;
	}
	public void setMsgId(int msgId) {
		this.msgId = msgId;
	}
	public int getFromId() {
		return fromId;
	}
	public void setFromId(int fromId) {
		this.fromId = fromId;
	}
	public String getFromName() {
		return fromName;
	}
	public void setFromName(String fromName) {
		this.fromName = fromName;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public Date getCtime() {
		return ctime;
	}
	public void setCtime(Date ctime) {
		this.ctime = ctime;
	}
	public int getMsgType() {
		return msgType;
	}
	public void setMsgType(int msgType) {
		this.msgType = msgType;
	}
	public int getMsgCount() {
		return msgCount;
	}
	public void setMsgCount(int msgCount) {
		this.msgCount = msgCount;
	}
	public String getFromFace() {
		return fromFace;
	}
	public void setFromFace(String fromFace) {
		this.fromFace = fromFace;
	}
	
	
}
